public class Location {
    private double pdi;
    private double idv;
    private double mas;
    private double uai;
    private double lto;
    private double ivr;

    public double getPdi() {
        return pdi;
    }

    public void setPdi(double pdi) {
        this.pdi = pdi;
    }

    public double getIdv() {
        return idv;
    }

    public void setIdv(double idv) {
        this.idv = idv;
    }

    public double getMas() {
        return mas;
    }

    public void setMas(double mas) {
        this.mas = mas;
    }

    public double getUai() {
        return uai;
    }

    public void setUai(double uai) {
        this.uai = uai;
    }

    public double getLto() {
        return lto;
    }

    public void setLto(double lto) {
        this.lto = lto;
    }

    public double getIvr() {
        return ivr;
    }

    public void setIvr(double ivr) {
        this.ivr = ivr;
    }
}
